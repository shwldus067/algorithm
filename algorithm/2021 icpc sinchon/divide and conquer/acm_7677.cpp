#include<cstdio>
const int mod=10000;
int main(){
	int arr[2][2]={{1, 1}, {1, 0}}, N;
	int a[2][2], res[2][2], tmp[2][2];
	while(1){
		scanf("%d", &N);
		if(N==-1)	break;
		res[0][0]=1, res[0][1]=0, res[1][0]=0, res[1][1]=1;
		a[0][0]=arr[0][0], a[0][1]=arr[0][1], a[1][0]=arr[1][0], a[1][1]=arr[1][1];
		while(N){
			if(N&1){
				tmp[0][0]=(res[0][0]*a[0][0]+res[0][1]*a[1][0])%mod;
				tmp[0][1]=(res[0][0]*a[0][1]+res[0][1]*a[1][1])%mod;
				tmp[1][0]=(res[1][0]*a[0][0]+res[1][1]*a[1][0])%mod;
				tmp[1][1]=(res[1][0]*a[0][1]+res[1][1]*a[1][1])%mod;
				res[0][0]=tmp[0][0], res[0][1]=tmp[0][1], res[1][0]=tmp[1][0], res[1][1]=tmp[1][1];
			}
			tmp[0][0]=(a[0][0]*a[0][0]+a[0][1]*a[1][0])%mod;
			tmp[0][1]=(a[0][0]*a[0][1]+a[0][1]*a[1][1])%mod;
			tmp[1][0]=(a[1][0]*a[0][0]+a[1][1]*a[1][0])%mod;
			tmp[1][1]=(a[1][0]*a[0][1]+a[1][1]*a[1][1])%mod;
			a[0][0]=tmp[0][0], a[0][1]=tmp[0][1], a[1][0]=tmp[1][0], a[1][1]=tmp[1][1];
			N>>=1;
		}
		printf("%d\n", res[0][1]);
	}
	return 0;
}
